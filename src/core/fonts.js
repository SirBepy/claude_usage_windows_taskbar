function drawDigit(pixels, size, digit, x, y, color, style = "classic") {
  const font = FONTS[style] || FONTS.classic;
  const glyph = font.glyphs[digit];
  if (!glyph) return;
  for (let row = 0; row < glyph.length; row++) {
    for (let col = 0; col < font.width; col++) {
      if ((glyph[row] >> (font.width - 1 - col)) & 1) {
        const px = x + col;
        const py = y + row;
        if (px >= 0 && px < size && py >= 0 && py < size) {
          const idx = (py * size + px) * 4;
          pixels[idx] = color[0];
          pixels[idx + 1] = color[1];
          pixels[idx + 2] = color[2];
          pixels[idx + 3] = 255;
        }
      }
    }
  }
}

function drawText(pixels, size, text, x, y, color, style = "classic") {
  const font = FONTS[style] || FONTS.classic;
  let curX = x;
  for (const char of String(text)) {
    drawDigit(pixels, size, parseInt(char, 10), curX, y, color, style);
    curX += font.width + 1; // digit width + 1 spacing
  }
}

module.exports = { FONTS, drawDigit, drawText };
